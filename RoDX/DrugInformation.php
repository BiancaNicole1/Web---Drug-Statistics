    <button class="open-popup">Details</button>
    <div class="popup">
        <div class="popup-content">
        <span class="close-button">&times;</span>
        <div class="popuptext">
            <p><strong>Cocaine</strong> is most commonly found as a white crystalline powder, and is usually snorted but is occasionally made into a solution and injected.</p>
            <p><strong>The effects</strong> of cocaine are similar to amphetamines and crack in that they create physical and mental arousal. When cocaine is snorted the effect is almost immediate and then peaks and fades within 15 - 30 minutes. This often results in users repeating their use almost every 20 minutes or so in order to maintain the desired effect.</p>
            <p>The snorting of cocaine can lead to mucosal constriction and eventually perforation of the nasal septum.</p>
