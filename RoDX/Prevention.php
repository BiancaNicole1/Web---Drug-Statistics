        <div class="drug-info">
            <h2>Risks</h2>
            <p>The risk of addiction and how fast you become addicted varies by drug. Some drugs, such as opioid painkillers, have a higher risk and cause addiction more quickly than others.

As time passes, you may need larger doses of the drug to get high. Soon you may need the drug just to feel good. As your drug use increases, you may find that it's increasingly difficult to go without the drug. Attempts to stop drug use may cause intense cravings and make you feel physically ill. These are called withdrawal symptoms.

Help from your health care provider, family, friends, support groups or an organized treatment program can help you overcome your drug addiction and stay drug-free.</p>
