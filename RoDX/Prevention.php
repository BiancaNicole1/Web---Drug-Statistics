                <li>Some strategies aim to do so by preventing drug use directly.</li>
                <li>Substance use disorders may be diagnosed as mild, moderate, or severe based on whether a person meets defined diagnostic criteria.</li>
                <li> By contrast, harm reduction approaches seek to reduce certain health and safety issues associated with drug use and to improve health and wellbeing during active drug use.</li>
