            <span>Complications</span>
            <button class="toggle-btn">+</button>
        </div>
        <div class="faq-answer">
            <p>Drug use can have significant and damaging short-term and long-term effects. Taking some drugs can be particularly risky, especially if you take high doses or combine them with other drugs or alcohol. Here are some examples.</p>
            <ul>
                <li>Methamphetamine, opiates and cocaine are highly addictive and cause multiple short-term and long-term health consequences, including psychotic behavior, seizures or death due to overdose. Opioid drugs affect the part of the brain that controls breathing, and overdose can result in death. Taking opioids with alcohol increases this risk.</li>
                <li>GHB and flunitrazepam may cause sedation, confusion and memory loss. These so-called "date rape drugs" are known to impair the ability to resist unwanted contact and recollection of the event. At high doses, they can cause seizures, coma and death. The danger increases when these drugs are taken with alcohol.</li>
                <li>MDMA — also known as molly or ecstasy — can interfere with the body's ability to regulate temperature. A severe spike in body temperature can result in liver, kidney or heart failure and death. Other complications can include severe dehydration, leading to seizures. Long-term, MDMA can damage the brain.</li>
                <li>One particular danger of club drugs is that the liquid, pill or powder forms of these drugs available on the street often contain unknown substances that can be harmful, including other illegally manufactured or pharmaceutical drugs.</li>
                <li>Due to the toxic nature of inhalants, users may develop brain damage of different levels of severity. Sudden death can occur even after a single exposure.</li>
